    WR1_PROG(REG_1420H, bswap_32big(0x0000003dU));
    WR1_PROG(REG_1458H, 0x00000000U);

    r_rsip_func101(bswap_32big(0x5c8c5894U), bswap_32big(0x8eddff6fU), bswap_32big(0x5c9a9cdeU), bswap_32big(0x81c99ba5U));
